#include <unistd.h>

void	ft_strcpy(char *str1, char *str2)
{
	while (*str2 != '\0')
	{
		*str1 = *str2;
		str1++;
		str2++;
	}
	*str1 = '\0';
}

void	ft_print_comb(void)
{
	char n[3];

	ft_strcpy(n, "012");
	while (n[0] <= '7')
	{
		while (n[1] <= '8')
		{
			while (n[2] <= '9')
			{
				if (n[0] != n[1] && n[1] != n[2])
				{
					write(1, n, 3);
					if (n[0] != '7')
					{
						write(1, ", ", 2);
					}
				}
				n[2]++;
			}
			n[2] = n[1] + 1;
			n[1]++;
		}
		n[1] = n[0] + 1;
		n[0]++;
	}
}
