int	ft_iterative_power(int nb, int power)
{
	int i;
	int n;

	i = 0;
	n = 1;
	if (power < 0)
	{
		return (0);
	}
	else
	{
		while (i < power)
		{
			n = n * nb;
			i++;
		}
	}
	return (n);
}
